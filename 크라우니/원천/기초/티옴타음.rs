// ═══════════════════════════════════════════════════════════════
// 티옴타음 — 4상 인식상태 체계
//
// ▲ 티(Ti): 확정 — 아는 것
// ● 옴(Om): 미확인 — 모르는 것
// ▼ 타(Ta): 오해 — 잘못 아는 것
// ◆ 음(Eum): 미인지 — 아는지조차 모르는 것
//
// 상태 전이: RTF1 기반
//   음→옴 (인지 시작), 옴→티 (확정), 타→옴 (오해 인지)
//   티→타 (환경변화), 티→옴 (유효기간만료)
// ═══════════════════════════════════════════════════════════════

use std::fmt;
use super::삼진수::트릿;

/// 4상 인식상태
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum 인식 {
    티,   // ▲ 확정: known & correct
    옴,   // ● 미확인: known unknown
    타,   // ▼ 오해: known but wrong
    음,   // ◆ 미인지: unknown unknown
}

impl 인식 {
    /// 신뢰도 수치 (-1.0 ~ +1.0)
    pub fn 신뢰도(&self) -> f64 {
        match self { 인식::티 => 1.0, 인식::옴 => 0.0, 인식::타 => -1.0, 인식::음 => -0.5 }
    }

    /// 트릿 매핑: 티→양, 옴→중, 타→음, 음→중(특수)
    pub fn 트릿값(&self) -> 트릿 {
        match self { 인식::티 => 트릿::양, 인식::옴 => 트릿::중, 인식::타 => 트릿::음, 인식::음 => 트릿::중 }
    }

    /// 인덱스 (셀 내 연결용): 티=0, 옴=1, 타=2, 음=3
    pub fn 번호(&self) -> usize {
        match self { 인식::티 => 0, 인식::옴 => 1, 인식::타 => 2, 인식::음 => 3 }
    }

    pub fn 에서(번호: usize) -> 인식 {
        match 번호 { 0 => 인식::티, 1 => 인식::옴, 2 => 인식::타, _ => 인식::음 }
    }

    // ═══ 상태 전이 (RTF1 기반) ═══

    /// 전진: 증거가 쌓여 더 확실해짐
    /// 음→옴, 옴→티, 타→옴
    pub fn 전진(&self) -> 인식 {
        match self {
            인식::음 => 인식::옴,   // 인지 시작
            인식::옴 => 인식::티,   // 확정
            인식::타 => 인식::옴,   // 오해 인지 → 재조사
            인식::티 => 인식::티,   // 이미 확정
        }
    }

    /// 후퇴: 반증되어 불확실해짐
    /// 티→옴 (유효기간만료), 옴→음, 타→타
    pub fn 후퇴(&self) -> 인식 {
        match self {
            인식::티 => 인식::옴,   // 확정 → 재확인 필요
            인식::옴 => 인식::음,   // 미확인 → 미인지로 퇴화
            인식::타 => 인식::타,   // 오해는 더 나빠지지 않음
            인식::음 => 인식::음,   // 이미 최저
        }
    }

    /// 반전: 반대 증거 발견
    /// 티→타 (환경변화로 틀려짐)
    pub fn 반전(&self) -> 인식 {
        match self {
            인식::티 => 인식::타,   // 확정이었으나 틀렸음
            인식::옴 => 인식::타,   // 조사 결과 틀렸음
            인식::타 => 인식::옴,   // 반전의 반전 = 재조사
            인식::음 => 인식::음,   // 모르는 건 반전 불가
        }
    }

    /// 두 인식상태의 합산: 더 불확실한 쪽을 따름
    pub fn 약한합(가: 인식, 나: 인식) -> 인식 {
        let 순서 = |x: &인식| -> i8 { match x { 인식::티 => 3, 인식::옴 => 1, 인식::타 => -1, 인식::음 => 0 } };
        if 순서(&가) <= 순서(&나) { 가 } else { 나 }
    }
}

impl fmt::Display for 인식 {
    fn fmt(&self, f: &mut fmt::Formatter) -> fmt::Result {
        match self {
            인식::티 => write!(f, "▲"),
            인식::옴 => write!(f, "●"),
            인식::타 => write!(f, "▼"),
            인식::음 => write!(f, "◆"),
        }
    }
}

#[cfg(test)]
mod 시험 {
    use super::*;

    #[test]
    fn 전이() {
        assert_eq!(인식::음.전진(), 인식::옴);
        assert_eq!(인식::옴.전진(), 인식::티);
        assert_eq!(인식::타.전진(), 인식::옴);
        assert_eq!(인식::티.후퇴(), 인식::옴);
        assert_eq!(인식::티.반전(), 인식::타);
        assert_eq!(인식::타.반전(), 인식::옴);
    }

    #[test]
    fn 약한합() {
        assert_eq!(인식::약한합(인식::티, 인식::옴), 인식::옴);
        assert_eq!(인식::약한합(인식::티, 인식::타), 인식::타);
        assert_eq!(인식::약한합(인식::옴, 인식::음), 인식::음);
    }
}
