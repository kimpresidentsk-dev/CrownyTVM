// ═══════════════════════════════════════════════════════════════
// 짜개(CodeGen) — AST → 크라우니어 명령열
// ═══════════════════════════════════════════════════════════════

use super::구문::노드;
use crate::크라우니어::명령어::명령;
use crate::기초::셀::셀값;
use crate::기초::티옴타음::인식;

pub struct 코드짜개 { 명령들: Vec<명령> }

impl 코드짜개 {
    pub fn 새것() -> Self { 코드짜개 { 명령들: Vec::new() } }

    fn 추가(&mut self, m: 명령) -> usize {
        let i = self.명령들.len(); self.명령들.push(m); i
    }
    fn 현재주소(&self) -> usize { self.명령들.len() }

    pub fn 생성(&mut self, ast: &노드) -> Result<Vec<명령>, String> {
        self.노드(&ast)?;
        Ok(self.명령들.clone())
    }

    fn 노드(&mut self, n: &노드) -> Result<(), String> {
        match n {
            // ═══ 값 ═══
            노드::정수(v)   => { self.추가(명령::넣기(셀값::정수(*v))); }
            노드::실수(v)   => { self.추가(명령::넣기(셀값::실수(*v))); }
            노드::문자열(s) => { self.추가(명령::넣기(셀값::문자열(s.clone()))); }
            노드::이름(s)   => { self.추가(명령::불러(s.clone())); }
            노드::삼진(v)   => { self.추가(명령::넣기(셀값::정수(*v as i64))); }
            노드::인식(s)   => {
                let i = match s.as_str() {
                    "확정" => 인식::티, "미확인" => 인식::옴,
                    "오해" => 인식::타, _ => 인식::음,
                };
                self.추가(명령::넣기(셀값::인식값(i)));
            }

            // ═══ 이항 연산 ═══
            노드::이항(좌, 연산, 우) => {
                self.노드(좌)?;
                self.노드(우)?;
                match 연산.as_str() {
                    "+"  => { self.추가(명령::더하기); }
                    "-"  => { self.추가(명령::빼기연산); }
                    "*"  => { self.추가(명령::곱하기); }
                    "/"  => { self.추가(명령::나누기); }
                    "%"  => { self.추가(명령::나머지); }
                    "==" => { self.추가(명령::같은가); }
                    "!=" => { self.추가(명령::다른가); }
                    ">"  => { self.추가(명령::큰가); }
                    "<"  => { self.추가(명령::작은가); }
                    ">=" => { self.추가(명령::크거나같은가); }
                    "<=" => { self.추가(명령::작거나같은가); }
                    "&&" => { self.추가(명령::삼진그리고); }
                    "||" => { self.추가(명령::삼진또는); }
                    "~~" => { self.추가(명령::삼진합의); }
                    _ => return Err(format!("알 수 없는 연산: {}", 연산)),
                }
            }

            // ═══ 단항 연산 ═══
            노드::단항(연산, 대상) => {
                self.노드(대상)?;
                match 연산.as_str() {
                    "!" => { self.추가(명령::삼진부정); }
                    _ => return Err(format!("알 수 없는 단항: {}", 연산)),
                }
            }

            // ═══ 변수 ═══
            노드::변수(이름, 값) => {
                self.노드(값)?;
                self.추가(명령::새저장(이름.clone()));  // 항상 새 셀
            }
            노드::대입(이름, 값) => {
                self.노드(값)?;
                self.추가(명령::저장(이름.clone()));    // 기존 셀 갱신
            }

            // ═══ 만약 (if/else — 3진 분기) ═══
            노드::만약(조건, 참블록, 거짓블록) => {
                self.노드(조건)?;
                if let Some(거짓) = 거짓블록 {
                    // 세갈래: 양→참블록, 음→거짓블록, 중→거짓블록
                    let 분기 = self.추가(명령::세갈래(0, 0)); // placeholder
                    // 중(옴) → 거짓 쪽으로 (fall through to 거짓)
                    let 중점프 = self.추가(명령::이동(0)); // placeholder
                    // 양(참) 블록
                    let 참주소 = self.현재주소();
                    self.노드(참블록)?;
                    let 끝점프 = self.추가(명령::이동(0)); // placeholder
                    // 음(거짓) 블록
                    let 거짓주소 = self.현재주소();
                    self.노드(거짓)?;
                    let 끝주소 = self.현재주소();
                    // 패치
                    self.명령들[분기] = 명령::세갈래(참주소, 거짓주소);
                    self.명령들[중점프] = 명령::이동(거짓주소);
                    self.명령들[끝점프] = 명령::이동(끝주소);
                } else {
                    // 조건 양이면 실행, 아니면 건너뛰기
                    let 분기 = self.추가(명령::세갈래(0, 0)); // placeholder
                    let 중점프 = self.추가(명령::이동(0)); // placeholder (중→건너뛰기)
                    let 참주소 = self.현재주소();
                    self.노드(참블록)?;
                    let 끝주소 = self.현재주소();
                    self.명령들[분기] = 명령::세갈래(참주소, 끝주소);
                    self.명령들[중점프] = 명령::이동(끝주소);
                }
            }

            // ═══ 판정 (3방향 분기) ═══
            노드::판정(조건, 양블록, 중블록, 음블록) => {
                self.노드(조건)?;
                let 분기 = self.추가(명령::세갈래(0, 0)); // placeholder
                // 중(옴) 블록 — fall through
                self.노드(중블록)?;
                let 중끝 = self.추가(명령::이동(0)); // placeholder
                // 양(+) 블록
                let 양주소 = self.현재주소();
                self.노드(양블록)?;
                let 양끝 = self.추가(명령::이동(0)); // placeholder
                // 음(-) 블록
                let 음주소 = self.현재주소();
                self.노드(음블록)?;
                let 끝주소 = self.현재주소();
                // 패치
                self.명령들[분기] = 명령::세갈래(양주소, 음주소);
                self.명령들[중끝] = 명령::이동(끝주소);
                self.명령들[양끝] = 명령::이동(끝주소);
            }

            // ═══ 동안 (while — 양이면 계속) ═══
            노드::동안(조건, 본문) => {
                let 시작 = self.현재주소();
                self.노드(조건)?;
                let 분기 = self.추가(명령::세갈래(0, 0)); // placeholder
                let 중점프 = self.추가(명령::이동(0)); // 중→탈출
                let 본문시작 = self.현재주소();
                self.노드(본문)?;
                self.추가(명령::이동(시작)); // 루프백
                let 끝주소 = self.현재주소();
                self.명령들[분기] = 명령::세갈래(본문시작, 끝주소);
                self.명령들[중점프] = 명령::이동(끝주소);
            }

            // ═══ 블록 ═══
            노드::블록(문장들) => {
                for s in 문장들 {
                    let 값남김 = matches!(s,
                        노드::호출(..) | 노드::내장함수(..) |
                        노드::이항(..) | 노드::단항(..) |
                        노드::정수(..) | 노드::실수(..) | 노드::문자열(..) |
                        노드::이름(..) | 노드::삼진(..) | 노드::인식(..) |
                        노드::배열(..) | 노드::인덱스(..) |
                        노드::파일읽기(..) | 노드::파일쓰기(..) |
                        노드::파일덧쓰기(..) | 노드::파일존재(..) |
                        노드::입력(..)
                    );
                    self.노드(s)?;
                    if 값남김 { self.추가(명령::빼기); }  // 스택 청소
                }
            }

            // ═══ 함수 정의 ═══
            노드::함수(이름, 인자들, 본문) => {
                // 정의 건너뛰기
                let 건너뛰기 = self.추가(명령::이동(0)); // placeholder
                let 함수주소 = self.현재주소();
                self.추가(명령::함수시작);
                // 인자 역순 저장 (스택에서 꺼내서 이름표에)
                for 인자 in 인자들.iter().rev() {
                    self.추가(명령::새저장(인자.clone()));  // 새 바인딩
                }
                self.노드(본문)?;
                self.추가(명령::넣기(셀값::없음)); // 기본 반환값
                self.추가(명령::돌아가기);
                let 끝주소 = self.현재주소();
                self.명령들[건너뛰기] = 명령::이동(끝주소);
                // 함수 이름에 주소 저장
                self.추가(명령::넣기(셀값::정수(함수주소 as i64)));
                self.추가(명령::저장(이름.clone()));
            }

            // ═══ 함수 호출 ═══
            노드::호출(이름, 인자들) => {
                // 시스템 내장 함수 체크
                match 이름.as_str() {
                    "목록" | "현재경로" | "체계" | "나누기" | "경로변경" => {
                        for a in 인자들 { self.노드(a)?; }
                        match 이름.as_str() {
                            "목록" => { self.추가(명령::디렉토리목록); }
                            "현재경로" => { self.추가(명령::현재경로); }
                            "체계" => { self.추가(명령::체계명령); }
                            "나누기" => { self.추가(명령::문자열나누기); }
                            "경로변경" => { self.추가(명령::경로변경); }
                            _ => unreachable!(),
                        }
                    }
                    _ => {
                        for a in 인자들 { self.노드(a)?; }
                        self.추가(명령::불러(이름.clone()));
                        self.추가(명령::호출(인자들.len()));
                    }
                }
            }

            // ═══ 반환 ═══
            노드::반환(값) => {
                self.노드(값)?;
                self.추가(명령::돌아가기);
            }

            // ═══ 출력 ═══
            노드::출력(값) => {
                self.노드(값)?;
                self.추가(명령::출력);
            }
            노드::출력값(값) => {
                self.노드(값)?;
                self.추가(명령::출력값);
            }

            // ═══ 온톨로지: 주장 ═══
            노드::주장(이름, 인자들) => {
                // 인자: 주체, 술어, 대상, 인식상태
                for a in 인자들 { self.노드(a)?; }
                self.추가(명령::주장생성);
                self.추가(명령::저장(이름.clone()));
            }

            // ═══ 온톨로지: 근거 ═══
            노드::근거(이름, _증거) => {
                self.추가(명령::넣기(셀값::문자열(이름.clone())));
                self.추가(명령::근거추가);
            }

            // ═══ 온톨로지: 전이 ═══
            노드::전이(이름, 방향) => {
                self.추가(명령::넣기(셀값::문자열(이름.clone())));
                match 방향.as_str() {
                    "전진" => { self.추가(명령::인식전진); }
                    "후퇴" => { self.추가(명령::인식후퇴); }
                    "반전" => { self.추가(명령::인식반전); }
                    _ => return Err(format!("알 수 없는 전이: {}", 방향)),
                }
            }

            // ═══ 파일 I/O ═══
            노드::파일읽기(경로) => {
                self.노드(경로)?;
                self.추가(명령::파일읽기);
            }
            노드::파일쓰기(경로, 내용) => {
                self.노드(경로)?;
                self.노드(내용)?;
                self.추가(명령::파일쓰기);
            }
            노드::파일덧쓰기(경로, 내용) => {
                self.노드(경로)?;
                self.노드(내용)?;
                self.추가(명령::파일덧쓰기);
            }
            노드::파일존재(경로) => {
                self.노드(경로)?;
                self.추가(명령::파일존재);
            }

            // ═══ 모듈 가져오기 ═══
            노드::가져오기(_경로) => {
                // 가져오기는 실행기에서 파일을 읽고 컴파일하여 실행
                // 코드젠 단계에서는 파일읽기+실행으로 변환
                // → 실제 구현은 주.rs의 모듈 로더에서 처리
            }

            // ═══ 문자열 내장함수 ═══
            노드::내장함수(이름, 인자들) => {
                for a in 인자들 { self.노드(a)?; }
                match 이름.as_str() {
                    "글자수" => { self.추가(명령::문자열글자수); }
                    "대문자" => { self.추가(명령::문자열대문자); }
                    "소문자" => { self.추가(명령::문자열소문자); }
                    "포함"   => { self.추가(명령::문자열포함); }
                    "글자"   => { self.추가(명령::문자열글자); }
                    "부분"   => { self.추가(명령::문자열부분); }
                    "숫자변환" => { self.추가(명령::숫자변환); }
                    "문자열변환" => { self.추가(명령::문자열변환); }
                    "추가"   => { self.추가(명령::배열추가); }
                    "길이"   => { self.추가(명령::배열길이); }
                    "설정"   => { self.추가(명령::배열설정); }
                    _ => return Err(format!("알 수 없는 내장함수: {}", 이름)),
                }
            }

            // ═══ 입력 ═══
            노드::입력(프롬프트) => {
                self.노드(프롬프트)?;
                self.추가(명령::사용자입력);
            }

            // ═══ 배열 ═══
            노드::배열(요소들) => {
                for e in 요소들 { self.노드(e)?; }
                self.추가(명령::배열생성(요소들.len()));
            }
            노드::인덱스(대상, 인덱스) => {
                self.노드(대상)?;
                self.노드(인덱스)?;
                self.추가(명령::배열읽기);
            }
        }
        Ok(())
    }
}
