// ═══════════════════════════════════════════════════════════════
// 명령어 — ISA729 기반 크라우니어 명령 체계
//
// 9섹터 × 9그룹 × 9옵코드 = 729
// 부트스트랩 단계: 섹터0 코어 81개 + 필수 확장
//
// 크라우니어(▲●▼◆)와 1:1 대응
// 한선씨가 이 명령어들로 컴파일됨
// ═══════════════════════════════════════════════════════════════

use crate::기초::셀::셀값;

/// VM 명령어 (한선씨 컴파일 결과)
#[derive(Debug, Clone)]
pub enum 명령 {
    // ═══ S0.G0 상태 (000~008) ═══
    넣기(셀값),           // PUSH: 값을 스택에
    빼기,                 // POP: 스택 탑 제거
    복사,                 // DUP: 스택 탑 복사
    바꾸기,               // SWAP: 스택 탑 2개 교환

    // ═══ S0.G3 변수 (027~035) ═══
    저장(String),          // STORE: 스택 탑 → 이름표 (기존 셀 갱신)
    새저장(String),        // NEW_STORE: 항상 새 셀 생성 (변수 선언용)
    불러(String),          // LOAD: 이름표 → 스택

    // ═══ S0.G4 산술 (036~044) ═══
    더하기,               // ADD
    빼기연산,             // SUB
    곱하기,               // MUL
    나누기,               // DIV
    나머지,               // MOD

    // ═══ S0.G5 비교 (045~053) ═══
    같은가,               // EQ
    다른가,               // NE
    큰가,                 // GT
    작은가,               // LT
    크거나같은가,          // GE
    작거나같은가,          // LE

    // ═══ S0.G1 3진연산 (009~017) ═══
    삼진부정,             // TNOT: 양↔음, 중 유지
    삼진그리고,           // TAND: min
    삼진또는,             // TOR: max
    삼진합의,             // TCONSENSUS: 같으면 유지, 다르면 중
    삼진비교,             // TCMP: 크면 양, 같으면 중, 작으면 음

    // ═══ S0.G6 흐름 (054~062) ═══
    세갈래(usize, usize),  // IF3: 양→주소1, 음→주소2, 중→다음
    이동(usize),           // JMP
    조건이동(usize),       // JT (양이면 이동)
    함수시작,              // FN_START (마커)
    호출(usize),           // CALL(인자수)
    돌아가기,              // RET
    정지,                  // HALT

    // ═══ 인식 (온톨로지 전용) ═══
    인식전진,              // 인식상태 전진 (음→옴→티)
    인식후퇴,              // 인식상태 후퇴 (티→옴→음)
    인식반전,              // 인식상태 반전 (티→타)
    인식조회,              // 인식상태 → 문자열

    // ═══ 주장 (온톨로지 Claim) ═══
    주장생성,              // 스택: [대상, 술어, 주체, 인식] → 주장 셀 생성
    근거추가,              // 주장에 증거 추가
    결정점수,              // 의사결정 점수 계산
    연결(String),          // 두 셀을 이름표로 연결

    // ═══ 체계 ═══
    출력,                  // PRINT (인식상태 포함)
    출력값,                // PRINT_VAL (값만)
    묶음만들기(usize),     // 배열 생성
    묶음꺼내기,            // 인덱스로 접근

    // ═══ 파일 I/O ═══
    파일읽기,              // 스택:[경로] → 내용 문자열 (실패시 오류문자열+타인식)
    파일쓰기,              // 스택:[내용, 경로] → 티/타
    파일덧쓰기,            // 스택:[내용, 경로] → 티/타
    파일존재,              // 스택:[경로] → 티/타(+1/-1)

    // ═══ 문자열 내장 ═══
    문자열글자수,           // 스택:[문자열] → 길이
    문자열포함,             // 스택:[검색, 대상] → 티/타
    문자열대문자,           // 스택:[문자열] → 대문자
    문자열소문자,           // 스택:[문자열] → 소문자

    // ═══ 입력 ═══
    사용자입력,             // 스택:[프롬프트] → 입력 문자열

    // ═══ OS 체계 ═══
    디렉토리목록,           // 스택:[경로] → 배열(파일명들)
    현재경로,               // → 문자열(현재 작업 디렉토리)
    경로변경,               // 스택:[경로] → 티/타
    체계명령,               // 스택:[명령문자열] → 출력 문자열
    문자열나누기,           // 스택:[구분자, 대상] → 배열

    // ═══ 셀프호스팅용 ═══
    문자열글자,             // 스택:[인덱스, 문자열] → 한 글자 문자열
    문자열부분,             // 스택:[끝, 시작, 문자열] → 부분문자열
    숫자변환,               // 스택:[문자열] → 정수
    문자열변환,             // 스택:[값] → 문자열

    // ═══ 배열 ═══
    배열생성(usize),        // 스택에서 n개 꺼내 배열 생성
    배열읽기,               // 스택:[인덱스, 배열] → 값
    배열추가,               // 스택:[값, 배열] → 새배열
    배열길이,               // 스택:[배열] → 길이
    배열설정,               // 스택:[값, 인덱스, 배열] → 새배열
}

/// ISA729 옵코드 번호 ↔ 명령 매핑 (부트스트랩용)
pub fn isa번호(번호: u16) -> Option<&'static str> {
    match 번호 {
        0 => Some("티"),        // TI
        1 => Some("옴"),        // OM
        2 => Some("타"),        // TA
        9 => Some("삼진부정"),   // TNOT
        10 => Some("삼진그리고"), // TAND
        11 => Some("삼진또는"),  // TOR
        12 => Some("삼진합의"),  // TCONSENSUS
        17 => Some("삼진비교"),  // TCMP
        36 => Some("더하기"),    // ADD
        37 => Some("빼기연산"),  // SUB
        38 => Some("곱하기"),    // MUL
        39 => Some("나누기"),    // DIV
        40 => Some("나머지"),    // MOD
        45 => Some("같은가"),    // EQ
        46 => Some("다른가"),    // NE
        47 => Some("큰가"),      // GT
        48 => Some("작은가"),    // LT
        63 => Some("만약"),      // IF
        65 => Some("동안"),      // WHILE
        72 => Some("함수"),      // FN
        76 => Some("출력"),      // WRITE
        _ => None,
    }
}
