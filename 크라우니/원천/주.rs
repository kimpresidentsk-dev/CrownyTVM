/// 모듈 전처리: 가져오기 "파일" → 파일 내용으로 치환
fn 모듈전처리(원천: &str, 기준파일: &str) -> String {
    let 기준경로 = std::path::Path::new(기준파일).parent().unwrap_or(std::path::Path::new("."));
    let mut 결과 = String::new();
    for 줄 in 원천.lines() {
        let 정리 = 줄.trim();
        if 정리.starts_with("가져오기") {
            // 가져오기 "경로" 파싱
            if let Some(시작) = 정리.find('"') {
                if let Some(끝) = 정리[시작+1..].find('"') {
                    let 경로 = &정리[시작+1..시작+1+끝];
                    let 전체경로 = 기준경로.join(경로);
                    match std::fs::read_to_string(&전체경로) {
                        Ok(내용) => {
                            결과.push_str(&format!("// === 가져오기: {} ===\n", 경로));
                            결과.push_str(&모듈전처리(&내용, 전체경로.to_str().unwrap_or("")));
                            결과.push_str(&format!("\n// === 끝: {} ===\n", 경로));
                        }
                        Err(e) => 결과.push_str(&format!("// 가져오기 실패: {} ({})\n", 경로, e)),
                    }
                    continue;
                }
            }
        }
        결과.push_str(줄);
        결과.push('\n');
    }
    결과
}

fn 컴파일실행(원천: &str) -> 실행기 {
    let mut r = 읽개::새것(원천);
    let t = r.분석().unwrap();
    let mut p = 문법파서::새것(t);
    let ast = p.분석().unwrap();
    let mut g = 코드짜개::새것();
    let code = g.생성(&ast).unwrap();
    let mut vm = 실행기::새것(code);
    let _ = vm.실행();
    vm
}

fn 실행(원천: &str) -> (Vec<String>, Result<셀값, String>) {
    let mut r = 읽개::새것(원천);
    let t = match r.분석() { Ok(v)=>v, Err(e) => return (vec![], Err(e)) };
    let mut p = 문법파서::새것(t);
    let ast = match p.분석() { Ok(v)=>v, Err(e) => return (vec![], Err(e)) };
    let mut g = 코드짜개::새것();
    let code = match g.생성(&ast) { Ok(v)=>v, Err(e) => return (vec![], Err(e)) };
    let mut vm = 실행기::새것(code);
    let result = vm.실행();
    (vm.출력, result)
}

