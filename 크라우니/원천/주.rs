fn 대화() {
    println!("\n  대화형 쉘 (종료: 끝 또는 Ctrl+D)\n");
    let mut 전체원천 = String::new();
    loop {
        eprint!("크라우니> ");
        let _ = std::io::Write::flush(&mut std::io::stderr());
        let mut 줄 = String::new();
        match std::io::stdin().read_line(&mut 줄) {
            Ok(0) => break,
            Ok(_) => {
                let 입력 = 줄.trim();
                if 입력 == "끝" || 입력 == "종료" { break; }
                if 입력.is_empty() { continue; }
                // 특수 명령
                if 입력 == "초기화" { 전체원천.clear(); println!("  (초기화됨)"); continue; }
                if 입력 == "원천" { println!("  {}", 전체원천); continue; }

                전체원천.push_str(입력);
                전체원천.push('\n');

                // 누적 실행
                let mut r = 읽개::새것(&전체원천);
                match r.분석() {
                    Ok(낱말들) => {
                        let mut p = 문법파서::새것(낱말들);
                        match p.분석() {
                            Ok(ast) => {
                                let mut g = 코드짜개::새것();
                                match g.생성(&ast) {
                                    Ok(code) => {
                                        let mut vm = 실행기::새것(code);
                                        match vm.실행() {
                                            Ok(결과) => {
                                                for o in &vm.출력 { println!("  {}", o); }
                                                if vm.출력.is_empty() {
                                                    let s = 결과.문자열로();
                                                    if s != "없음" { println!("  → {}", s); }
                                                }
                                            }
                                            Err(e) => { eprintln!("  실행 오류: {}", e); 전체원천.truncate(전체원천.len() - 입력.len() - 1); }
                                        }
                                    }
                                    Err(e) => { eprintln!("  생성 오류: {}", e); 전체원천.truncate(전체원천.len() - 입력.len() - 1); }
                                }
                            }
                            Err(e) => { eprintln!("  분석 오류: {}", e); 전체원천.truncate(전체원천.len() - 입력.len() - 1); }
                        }
                    }
                    Err(e) => { eprintln!("  읽기 오류: {}", e); 전체원천.truncate(전체원천.len() - 입력.len() - 1); }
                }
            }
            Err(_) => break,
        }
    }
    println!("\n  ▲●▼◆ 안녕히!");
}

