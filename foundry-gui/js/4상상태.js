const EP = Object.freeze({
  TI:   2,    // ▲확정
  OM:   0,    // ●미확인
  TA:  -2,    // ▼오해
  EUM: -1,    // ◆미인지
});

const 사상 = {
  확정:    1,
  미확인:  0,
  오류:   -1,
  미인지: -2,

  이름(v) {
    if (v >=  1) return '확정';
    if (v ===  0) return '미확인';
    if (v === -1) return '오류';
    return '미인지';
  },

  클래스(v) { return this.이름(v); },

  HTTP변환(code) {
    if (code >= 200 && code < 300) return 1;
    if (code === 202) return 0;
    if (code === 404) return -2;
    return -1;
  },

  // 4상 EP(2/-1/-2/0) → 사상(1/0/-1/-2) 변환
  EP변환(ep) {
    if (ep === EP.TI)  return 1;
    if (ep === EP.OM)  return 0;
    if (ep === EP.TA)  return -1;
    return -2;
  },

  집계(arr) {
    if (!arr.length) return -2;
    if (arr.some(v => v === -1)) return -1;
    if (arr.every(v => v >= 1))  return 1;
    if (arr.some(v => v === 0))  return 0;
    return -2;
  },

  뱃지(v) {
    return `<span class="뱃지 ${this.클래스(v)}">${this.이름(v)}</span>`;
  }
};

